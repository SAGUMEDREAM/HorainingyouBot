    public static String parse(String inputText) {
        StringBuilder sb = new StringBuilder();

        inputText = replaceNumbers(inputText);

        List<String> pinyinList = PinyinUtil.toPinyinList(inputText);

        for (String pinyin : pinyinList) {
            String kana = mapping.get(pinyin);
            if (kana == null) {
                continue;
            }
            sb.append(kana);
        }

        return sb.toString();
    }

    private static String replaceNumbers(String text) {
        Pattern pattern = Pattern.compile("\\d+");
        Matcher matcher = pattern.matcher(text);

        StringBuilder sb = new StringBuilder();

        while (matcher.find()) {
            long num = Long.parseLong(matcher.group());
            String chinese = NumberChineseFormatter.format(num, false);
            matcher.appendReplacement(sb, chinese);
        }

        matcher.appendTail(sb);

        return sb.toString();
    }

